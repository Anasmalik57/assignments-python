# 1-> A tuple is an ordered, immutable collection of items.
# 2-> Tuples can contain elements of different data types, and duplicates are allowed.
# 3-> Since they are immutable, their elements cannot be changed after creation.

my_tuple = (1, "hello", 3.14, "anas")
print(my_tuple)   # (1, 'hello', 3.14, 'anas')
